function recriaReordenaIds() {
    let ids = FUNCOES_SISTEMA.map((item) => item.id).sort((a, b) => a - b);
    let idsReordenados = ids.map((item, index) => {
        return { id: item, novoId: index + 1 };
    });
    let funcoesSistema = FUNCOES_SISTEMA.map((item) => {
        let novoId = idsReordenados.find((id) => id.id === item.id).novoId;
        return { ...item, id: novoId };
    });
    
    return funcoesSistema;
}

